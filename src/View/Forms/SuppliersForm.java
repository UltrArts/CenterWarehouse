    private void setData(){
        int index = suppTbl.getRowIndex(tblSuppliers);
        txtId.setText(tblmodel.getValueAt(index, 0).toString());
        txtName.setText(tblmodel.getValueAt(index, 1).toString());
        cbproductType.setSelectedItem(tblmodel.getValueAt(index, 2).toString());
        txtContact.setText(tblmodel.getValueAt(index, 3).toString());
        txtEmail.setText(tblmodel.getValueAt(index, 4).toString());
        cbCountry.setSelectedItem(tblmodel.getValueAt(index, 5).toString());
        txtCountryCod.setText(tblmodel.getValueAt(index, 6).toString());
        txtAddress.setText(tblmodel.getValueAt(index, 7).toString());
        txtNuit.setText(tblmodel.getValueAt(index, 8).toString());
        txtCep.setText(tblmodel.getValueAt(index, 9).toString());
        
        if(Integer.parseInt(tblmodel.getValueAt(index, 0).toString()) > 0){
            lblSave.setText("Actualizar");
        }else{
            lblSave.setText("Guardar");
        }
            
    }
    
    
    
    private void mouseEntered(JLabel lbl, boolean val){
        if(val)
            lbl.setForeground(Color.green);
        else
            lbl.setForeground(Color.red);
    }
    
    private void mouseExited(JLabel lbl){
        lbl.setForeground(Color.WHITE);
    }
    
    
    private boolean setUp1(){
        try{
            supp = new Supplier(txtName.getText(),txtContact.getText(), cbCountry.getSelectedItem().toString(), txtEmail.getText(),
                Integer.parseInt(txtCountryCod.getText()), Integer.parseInt(txtNuit.getText()),txtCep.getText(), cbproductType.getSelectedItem().toString(), txtAddress.getText() );
            return true;
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Atente-se aos compos obrigatórios!");
        }
        return false;
    }
    
