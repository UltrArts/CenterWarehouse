    private void save(){
        if(setUp1()){
        suppCtrl = new SupplierController();
            if(suppCtrl.save(supp)){
                cleanUp();
                populateTable();
            }
        }
    }
    
    private void update(){
        if(setUp2()){
            suppCtrl = new SupplierController();
            if(suppCtrl.update(supp)){
                cleanUp();
                populateTable();
            }
        }
    }
    
    private void cleanUp(){
        txtAddress.setText(null);
        txtName.setText(null);
        txtNuit.setText(null);
        txtEmail.setText(null);
        txtCep.setText(null);
        txtContact.setText(null);
        txtCountryCod.setText(null);
        cbCountry.setSelectedIndex(0);
        cbproductType.setSelectedIndex(0);
        txtId.setText(null);
    }

