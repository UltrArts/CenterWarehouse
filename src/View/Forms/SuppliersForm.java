    private void populateTable(){
        tblmodel =  (DefaultTableModel) tblSuppliers.getModel();
        suppTbl = new SupplierTable(tblmodel);
        tblSuppliers.setRowSorter(new TableRowSorter(tblmodel));
        while(tblSuppliers.getRowCount() > 0)
            tblmodel.removeRow(0);
        suppTbl.list();
    }
    
    private void init(){
        tblSuppliers.setShowHorizontalLines(true);
        tblSuppliers.setGridColor(new Color(230, 230, 230));
        tblSuppliers.setRowHeight(35);
        tblSuppliers.getTableHeader().setReorderingAllowed(false);
//        tblUsers.set
        tblSuppliers.setFont(new Font("times new roman", Font.PLAIN, 18));
        populateTable();
    }
    
